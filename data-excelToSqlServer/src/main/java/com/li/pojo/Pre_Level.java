package com.li.pojo;

public class Pre_Level {
	private String pre_code;
	private String pre_name;
	private String pre_subcode;
	public Pre_Level() {
		super();
	}
	public String getPre_code() {
		return pre_code;
	}
	public void setPre_code(String pre_code) {
		this.pre_code = pre_code;
	}
	public String getPre_name() {
		return pre_name;
	}
	public void setPre_name(String pre_name) {
		this.pre_name = pre_name;
	}
	public String getPre_subcode() {
		return pre_subcode;
	}
	public void setPre_subcode(String pre_subcode) {
		this.pre_subcode = pre_subcode;
	}
	
}
